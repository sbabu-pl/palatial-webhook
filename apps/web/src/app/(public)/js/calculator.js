function calculateInsuranceMaturity(product, sumAssured, term) {
  const rates = CONFIG.bonusRates[product] || { annual: 0, final: 0 };

  const totalReversionaryBonus = sumAssured * rates.annual * term;
  const finalBonus = sumAssured * rates.final;
  const maturityValue = sumAssured + totalReversionaryBonus + finalBonus;

  return { totalReversionaryBonus, finalBonus, maturityValue };
}

function getEffectiveMonthlyPremium(frequency, premium) {
  if (frequency === "Quarterly") return premium / 3;
  if (frequency === "Half-Yearly") return premium / 6;
  if (frequency === "Yearly") return premium / 12;
  return premium;
}

function getAffordabilityIndicator(frequency, premium, monthlyBudget) {
  if (!monthlyBudget || monthlyBudget <= 0) {
    return {
      label: "Not Available",
      note: "Enter a monthly budget to see affordability guidance.",
    };
  }

  const effectiveMonthlyPremium = getEffectiveMonthlyPremium(frequency, premium);
  const ratio = effectiveMonthlyPremium / monthlyBudget;

  if (ratio <= 0.35) {
    return {
      label: "Comfortable",
      note: "This premium appears comfortably within the stated monthly budget.",
    };
  }

  if (ratio <= 0.6) {
    return {
      label: "Manageable",
      note: "This premium may be manageable, but the client should review other financial commitments.",
    };
  }

  if (ratio <= 0.85) {
    return {
      label: "Stretched",
      note: "This premium may stretch the stated budget. A lower sum assured or different term may be worth comparing.",
    };
  }

  return {
    label: "High",
    note: "This premium appears high relative to the stated monthly budget. Consider reducing cover or adjusting the payment structure.",
  };
}

function calculateIRRFromCashflows(cashflows, guess = 0.01) {
  let rate = guess;

  for (let i = 0; i < 100; i += 1) {
    let npv = 0;
    let derivative = 0;

    for (let t = 0; t < cashflows.length; t += 1) {
      npv += cashflows[t] / Math.pow(1 + rate, t);
      derivative += (-t * cashflows[t]) / Math.pow(1 + rate, t + 1);
    }

    if (Math.abs(derivative) < 1e-10) return null;

    const nextRate = rate - npv / derivative;
    if (!Number.isFinite(nextRate)) return null;
    if (Math.abs(nextRate - rate) < 1e-7) return nextRate;

    rate = nextRate;
  }

  return rate;
}

function calculateAnnualizedIRR(premium, frequency, term, maturityValue) {
  const periodsPerYear = CONFIG.paymentsPerYear[frequency];
  if (!periodsPerYear) return null;

  const totalPeriods = periodsPerYear * term;
  const cashflows = Array.from({ length: totalPeriods }, () => -premium);
  cashflows.push(maturityValue);

  const periodicIRR = calculateIRRFromCashflows(cashflows);
  if (periodicIRR === null || periodicIRR <= -1) return null;

  return (Math.pow(1 + periodicIRR, periodsPerYear) - 1) * 100;
}

