function validateCalculatorInputs({ product, age, term, sumAssured, frequency }) {
  if (!product || !age || !term || !sumAssured || !frequency) {
    return "Please fill all required fields.";
  }

  if (age < 17 || age > 84) {
    return "Please enter a valid age between 17 and 84.";
  }

  if (term < 5 || term > 20) {
    return "Term must be between 5 and 20 years.";
  }

  if (sumAssured <= 0) {
    return "Sum assured must be greater than zero.";
  }

  if (!premiumTables[product]) {
    return "Invalid product selected.";
  }

  if (!CONFIG.frequencyFactors[frequency] || !CONFIG.paymentsPerYear[frequency]) {
    return "Invalid payment frequency.";
  }

  return null;
}

function buildCalculation(inputs) {
  const { product, age, term, sumAssured, monthlyBudget, frequency } = inputs;
  const anb = age + 1;

  const rateRow = premiumTables[product]?.[anb];
  if (!rateRow) throw new Error("No premium rate found for this age.");

  const rate = rateRow[term];
  if (rate === undefined || rate === null) throw new Error("No premium rate found for this term.");

  const factor = CONFIG.frequencyFactors[frequency];
  const periodsPerYear = CONFIG.paymentsPerYear[frequency];

  const premium = (sumAssured / 1000) * rate * factor;
  const annualPremium = premium * periodsPerYear;
  const totalContribution = annualPremium * term;

  const maturity = calculateInsuranceMaturity(product, sumAssured, term);
  const affordability = getAffordabilityIndicator(frequency, premium, monthlyBudget);
  const irr = calculateAnnualizedIRR(premium, frequency, term, maturity.maturityValue);
  const recommendation = getRecommendation(product, age, term, sumAssured, annualPremium);
  const aiAdvisor = getAIAdvisorText(
    product,
    age,
    term,
    sumAssured,
    annualPremium,
    maturity.maturityValue
  );

  return {
    product,
    age,
    anb,
    term,
    sumAssured,
    monthlyBudget,
    frequency,
    premium,
    annualPremium,
    totalContribution,
    totalReversionaryBonus: maturity.totalReversionaryBonus,
    finalBonus: maturity.finalBonus,
    estimatedMaturityValue: maturity.maturityValue,
    affordabilityLabel: affordability.label,
    affordabilityNote: affordability.note,
    irr,
    recommendation,
    aiAdvisor,
    calculatedAt: new Date().toISOString(),
  };
}

